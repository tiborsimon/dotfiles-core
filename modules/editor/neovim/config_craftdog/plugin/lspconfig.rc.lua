-- Typescript LSP server
-- External package needs to be installed: npm install -g typescript-language-server typescript
nvim_lsp.tsserver.setup {
  on_atach = on_attach,
  filetypes = { "typescript", "typescriptreact", "typescritp.tsx" },
  cmd = { "typescript-language-server", "--stdio" }
}

-- Lua LSP server
-- It has an external dependencie called lua-language-server that needs to be installed via brew.
nvim_lsp.sumneko_lua.setup {
  on_attach = on_attach,
  settings = {
    Lua = {
      diagnostics = {
        -- Get the language server to recognize the 'vim' global
        globals = { 'vim' }
      },

      workspace = {
        -- Make the server aware of Neovim runtime files
        library = vim.api.nvim_get_runtime_file("", true),
        checkThirdParty = false
      },
    },
  },
}
